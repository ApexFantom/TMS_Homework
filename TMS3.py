# Задание 1: поменять местами слова в предложении, добавить восклицательный знак в начало и конец, слова разделить через " ! ", выполнить разныые способы форматирования

a = input("Введите предложение состоящее из двух слов: \n")
ast = a.split(" ")
ast = ast[0:2]
ast.reverse()
a = " ! ".join(ast)
a = '!' + a + '!'
print("Мы получили строку: {} {}".format(ast[1], ast[0]))
print("Мы поменяли их местами: {0} {1}".format(ast[0], ast[1]))
print(f"Мы добавили !: {a}")
print()

