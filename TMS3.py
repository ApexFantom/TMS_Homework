# Задание 5: Сделать программу, в которой нужно угадывать число

import random
s = input("угадайте число от 0 до 10, введя его: ")
r = random.sample(range(0, 10), 1)
if s == r: print(f"Вау, реально угадал! это и правда {r}")
else: print(f"не угадал, я загадал число: {r}")